// Handler holds all HTTP handlers and their dependencies.
type Handler struct {
	createOrder  *command.CreateOrderHandler
	addItem      *command.AddItemHandler
	confirmOrder *command.ConfirmOrderHandler
	getOrder     *query.GetOrderHandler
	listOrders   *query.ListOrdersHandler
	validator    *RequestValidator
	logger       *zap.SugaredLogger
}

func NewHandler(
	createOrder *command.CreateOrderHandler,
	addItem *command.AddItemHandler,
	confirmOrder *command.ConfirmOrderHandler,
	getOrder *query.GetOrderHandler,
	listOrders *query.ListOrdersHandler,
	logger *zap.SugaredLogger,
) *Handler {
	return &Handler{
		createOrder:  createOrder,
		addItem:      addItem,
		confirmOrder: confirmOrder,
		getOrder:     getOrder,
		listOrders:   listOrders,
		validator:    NewRequestValidator(),
		logger:       logger,
	}
}

// RegisterRoutes sets up chi routes.
func (h *Handler) RegisterRoutes(r chi.Router) {
	// Swagger UI
	r.Get("/swagger/*", httpSwagger.Handler(
		httpSwagger.URL("/swagger/doc.json"), //The url pointing to API definition
	))

	r.Route("/orders", func(r chi.Router) {
		r.Post("/", h.HandleCreateOrder)
		r.Get("/", h.HandleListOrders)
		r.Route("/{orderID}", func(r chi.Router) {
			r.Get("/", h.HandleGetOrder)
			r.Post("/items", h.HandleAddItem)
			r.Post("/confirm", h.HandleConfirmOrder)
		})
	})
}

// @Summary Create a new order
// @Description Create an order for a given customer
// @Tags orders
// @Accept json
// @Produce json
// @Param request body dto.CreateOrderRequest true "Create Order Request"
// @Success 201 {object} dto.CreateOrderResult
// @Failure 400 {object} dto.ErrorResponse
// @Failure 500 {object} dto.ErrorResponse
// @Router /orders [post]
func (h *Handler) HandleCreateOrder(w http.ResponseWriter, r *http.Request) {
	var req dto.CreateOrderRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		writeError(w, r, http.StatusBadRequest, "INVALID_REQUEST", "Invalid JSON body", nil)
		return
	}

	if errs := h.validator.Validate(req); errs != nil {
		writeError(w, r, http.StatusBadRequest, "VALIDATION_ERROR", "Request validation failed", errs)
		return
	}

	orderID, err := h.createOrder.Handle(r.Context(), command.CreateOrder{
		CustomerID: req.CustomerID,
	})
	if err != nil {
		mapAndWriteError(w, r, err)
		return
	}

	writeJSON(w, http.StatusCreated, dto.CreateOrderResult{OrderID: orderID})
}

// @Summary Add an item to an order
// @Description Add a new product to an existing DRAFT order
// @Tags orders
// @Accept json
// @Produce json
// @Param orderID path string true "Order ID"
// @Param request body dto.AddItemRequest true "Add Item Request"
// @Success 200 {object} map[string]string
// @Failure 400 {object} dto.ErrorResponse
// @Failure 404 {object} dto.ErrorResponse
// @Failure 409 {object} dto.ErrorResponse
// @Failure 500 {object} dto.ErrorResponse
// @Router /orders/{orderID}/items [post]
func (h *Handler) HandleAddItem(w http.ResponseWriter, r *http.Request) {
	orderID := chi.URLParam(r, "orderID")

	var req dto.AddItemRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		writeError(w, r, http.StatusBadRequest, "INVALID_REQUEST", "Invalid JSON body", nil)
		return
	}

	if errs := h.validator.Validate(req); errs != nil {
		writeError(w, r, http.StatusBadRequest, "VALIDATION_ERROR", "Request validation failed", errs)
		return
	}

	err := h.addItem.Handle(r.Context(), command.AddItemToOrder{
		OrderID:     orderID,
		ProductID:   req.ProductID,
		ProductName: req.ProductName,
		Quantity:    req.Quantity,
		UnitPrice:   req.UnitPrice,
	})
	if err != nil {
		mapAndWriteError(w, r, err)
		return
	}

	writeJSON(w, http.StatusOK, map[string]string{"message": "item added"})
}

