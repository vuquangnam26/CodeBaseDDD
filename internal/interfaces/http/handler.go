// @Summary List orders
// @Description Get a paginated list of orders, optionally filtered
// @Tags orders
// @Accept json
// @Produce json
// @Param status query string false "Filter by order status"
// @Param customer_id query string false "Filter by customer ID"
// @Param page query int false "Page number" default(1)
// @Param size query int false "Page size" default(20)
// @Param sort query string false "Sort field (created_at, total_amount)"
// @Param dir query string false "Sort direction (asc, desc)"
// @Success 200 {object} dto.PaginatedOrdersResponse
// @Failure 500 {object} dto.ErrorResponse
// @Router /orders [get]
