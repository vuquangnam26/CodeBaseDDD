// RegisterGinRoutes registers all Gin routes.
func (h *Handler) RegisterGinRoutes(r *gin.Engine) {
	// Swagger UI - wrap the http.HandlerFunc for Gin
	r.GET("/swagger/*any", gin.WrapF(httpSwagger.WrapHandler))

	// Health endpoints (not in handler group)
	orders := r.Group("/orders")
	{
		orders.POST("", h.GinHandleCreateOrder)
		orders.GET("", h.GinHandleListOrders)

		orders.GET("/:orderID", h.GinHandleGetOrder)
		orders.POST("/:orderID/items", h.GinHandleAddItem)
		orders.POST("/:orderID/confirm", h.GinHandleConfirmOrder)
	}
}

// mapAndWriteGinError maps domain errors to HTTP responses for Gin.
func (h *Handler) mapAndWriteGinError(c *gin.Context, err error) {
	h.logger.Errorw("handler error",
		"error", err.Error(),
		"type", "domain_error",
	)
	mapAndWriteError(c.Writer, c.Request, err)
}
